    function test_OUGP_filter()

    close all

    [t,y] = importTestData();
    lowCut = [0.0005 0.005 0.01 0.02 0.04];
    hiCut = [0.4 0.6 0.8 1.0 2.0];    
    freqRes = [0.0005 0.001 0.005 0.01 0.05 0.1];    

    parameters = []

    inputIsDetrended = 1;
    if inputIsDetrended == 1
        y = detrend(y, 'linear'); 
        % OUGP filters and does detrending, so making the frequency
        % response behavior more comparable to classical approach, detrend
        % first the input
    end
    
    notBandpass = 0;

    % when not varied
    lowIndex = 2;
    hiIndex = 2;
    resIndex = 4;

    %% MODIFY LOW-CUT FREQUENCY
    if inputIsDetrended == 1    
        legStrPeriod{1} = 'Input (detrended)';
    else
        legStrPeriod{1} = 'Input';
    end

    for lo = 1 : length(lowCut)

        % smooth / filter
        if notBandpass == 1
            [yFilt(:,lo), nyqFreq] = ougp_gpousmooth2(t, y, lowCut(lo), 'hp');
        else
            [yFilt(:,lo), nyqFreq] = ougp_gpousmooth2(t, y, [lowCut(lo) hiCut(hiIndex)], 'bp');
            %[yFilt(:,lo), nyqFreq] = ougp_gpousmooth2(t, y, lowCut(lo), 'hp');
            %[yFilt(:,lo), nyqFreq] = ougp_gpousmooth2(t, yFilt(:,lo), hiCut(hiIndex), 'lp');
        end

        % LOMB-SC
        F = lowCut(lowIndex) : freqRes(resIndex) : hiCut(hiIndex);

            % Input
            [Pr, fr, PSD_In, z0, A0, ofac] = ougp_flspw(t, y, F);

            % Output
            [Pr, fr, PSD, z0, A0, ofac] = ougp_flspw(t, yFilt(:,lo), F);        
            
            legStrRR{lo} = [num2str(lowCut(lo)), 'Hz'];
            legStrPeriod{lo+1} = [num2str(lowCut(lo)), 'Hz']; %, f_n_y_q = ', num2str(nyqFreq(lo))]

            %PSD = PSD(1:length(F)-1); % trim
            %size(PSD)
            PSD_filt(lo,:) = PSD;

    end    

    if notBandpass == 1
        titleRR = sprintf('%s\n%s', ['Detrended and high-pass filtered R-R series'], ['f_N_y_q = ', num2str(nyqFreq, '%1.3f'), ' Hz']);
    else
        titleRR = sprintf('%s\n%s', ['Detrended and bandpass (lp ', num2str(hiCut(hiIndex)),'Hz +hp) filtered R-R series'], ['f_N_y_q = ', num2str(nyqFreq, '%1.3f'), ' Hz']);        
    end

    % PLOT
    plot_RR_filtering(t,y,yFilt,F(1:end-1),PSD_filt,PSD_In,nyqFreq,parameters,lowCut,hiCut(hiIndex),freqRes(resIndex),notBandpass,titleRR, legStrRR,legStrPeriod, 'lowCut')

    %% MODIFY HI-CUT FREQUENCY
    if inputIsDetrended == 1    
        legStrPeriod2{1} = 'Input (detrended)';
    else
        legStrPeriod2{1} = 'Input';
    end
    for hi = 1 : length(hiCut)

        % smooth / filter
        if notBandpass == 1
            [yFilt(:,lo), nyqFreq] = ougp_gpousmooth2(t, y, hiCut(hi), 'lp');
        else
            [yFilt(:,hi), nyqFreq] = ougp_gpousmooth2(t, y, [lowCut(lowIndex) hiCut(hi)], 'bp');
            [yFilt(:,hi), nyqFreq] = ougp_gpousmooth2(t, y, lowCut(lowIndex), 'hp');
            [yFilt(:,hi), nyqFreq] = ougp_gpousmooth2(t, yFilt(:,hi), hiCut(hi), 'lp');
        end

        % LOMB-SC
        F = lowCut(lowIndex) : freqRes(resIndex) : hiCut(hiIndex);

            % Input
            [Pr, fr, PSD_In, z0, A0, ofac] = ougp_flspw(t, y, F);

            % Output
            [Pr, fr, PSD(hi,:), z0, A0, ofac] = ougp_flspw(t, yFilt(:,hi), F);

            legStrRR2{hi} = [num2str(hiCut(hi)), 'Hz'];
            legStrPeriod2{hi+1} = [num2str(hiCut(hi)), 'Hz']; %, f_n_y_q = ', num2str(nyqFreq(lo))]

    end

    if notBandpass == 1
        titleRR = sprintf('%s\n%s', ['Detrended and low-pass filtered R-R series'], ['f_N_y_q = ', num2str(nyqFreq, '%1.3f'), ' Hz']);
    else
        titleRR = sprintf('%s\n%s', ['Detrended and bandpass (hp + lp ', num2str(lowCut(lowIndex)), 'Hz) filtered R-R series'], ['f_N_y_q = ', num2str(nyqFreq, '%1.3f'), ' Hz']);        
    end
    
    % PLOT
    plot_RR_filtering(t,y,yFilt,F(1:end-1),PSD,PSD_In,nyqFreq,parameters,lowCut(lowIndex),hiCut,freqRes(resIndex),notBandpass,titleRR,legStrRR2,legStrPeriod2,'hiCut')

    
    
    %% MODIFY FREQUENCY RESOLUTION
    %{
    for res = 1 : length(freqRes)

        % smooth / filter
        [yFilt, nyqFreq] = ougp_gpousmooth2(t, y, [lowCut(lowIndex) hiCut(hiIndex)], 'bp');

        % LOMB-SC
        F_res{res} = lowCut(lowIndex) : freqRes(resIndex) : hiCut(hiIndex);

            % Input
            [Pr, fr, PSD_In2{res}, z0, A0, ofac] = ougp_flspw(t, y, F_res{res});

            % Output
            [Pr, fr, PSD_res{res}, z0, A0, ofac] = ougp_flspw(t, yFilt, F_res{res});

    end

    % PLOT
    plot_RR_filtering(t,y,yFilt,F_res,PSD_res,PSD_In2,nyqFreq,parameters,lowCut(lowIndex),hiCut(hiIndex),freqRes,'freqRes')
    %}


function plot_RR_filtering(thp,hp,hp4,F,PSD,PSD_In,nyqFreq,parameters,lowCut,hiCut,freqRes,notBandpass,titleRR,legStrRR,legStrPeriod,simulation)

